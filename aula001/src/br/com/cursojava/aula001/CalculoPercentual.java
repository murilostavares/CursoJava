public class CalculoPercentual {
		public static void main(String[] args) {
		
		double percentual = 13.5 / 100;
		double valor = 870.50;
		double resultado = valor * percentual;
		
		System.out.printf("%s %f %s %f %s \n", "O Percentual correspondente a 13,5% de", valor, "é igual a", resultado, ".");
		
	

	}

}
