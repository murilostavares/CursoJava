package br.com.cursojava.aula001;

public class AreaRetangulo {
	public static void main(String[] args) {
		int base = 8;
		int altura = 15;
		int area;
		
		area = base * altura;
		
		System.out.println("A area de um triangulo com base 8 e altura 15:");
		System.out.println("");
		System.out.println(area);
		System.out.println("");
		System.out.printf("%d x %d = %d",  base, altura, area);
		
	}
	
}
